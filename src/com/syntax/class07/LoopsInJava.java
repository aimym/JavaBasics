package com.syntax.class07;

public class LoopsInJava {
public static void main(String[] args) {
	
	/*
	 * Loops Statements -- repeats block of code as long a condition is true
	 * 
	 * 4. Loops in Java
	 *    while
	 *    do while
	 *    for while
	 *    enchanced for loop/ advanced for loop/ for each loop
	 */
	
	//while is loop--> reapeats block of code as long as condition is true
	
	int a=11;
	
	if (a<12) {
		System.out.println("Morning");
	}  //it prints 1 once
	
	int b=11;
	
	while (b<12) {
		System.out.println("Morning");
	}//it'll keep printing forever 
	
	
	
	
	
	
	
	
}
}
